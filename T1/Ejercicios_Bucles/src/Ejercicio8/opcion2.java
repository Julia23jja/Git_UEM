/*
Algoritmo que muestre la tabla de multiplicar de los números 
1,2,3,4 y 5.
 */

package Ejercicio8;

public class opcion2 {

	static final int UNO = 1;
	static final int DOS = 2;
	static final int TRES = 3;
	static final int CUATRO = 4;
	static final int CINCO = 5;

	public static void main(String[] args) {

		// UNO
		for (int i = 1; i <= 10; i++) {
			System.out.println(UNO + " x " + i + " = " + (UNO * i));
		}
		System.out.println("\n");

		// DOS
		for (int i = 1; i <= 10; i++) {
			System.out.println(DOS + " x " + i + " = " + (DOS * i));
		}
		System.out.println("\n");

		// TRES
		for (int i = 1; i <= 10; i++) {
			System.out.println(TRES + " x " + i + " = " + (TRES * i));
		}
		System.out.println("\n");

		// CUATRO
		for (int i = 1; i <= 10; i++) {
			System.out.println(CUATRO + " x " + i + " = " + (CUATRO * i));
		}
		System.out.println("\n");

		// CINCO
		for (int i = 1; i <= 10; i++) {
			System.out.println(CINCO + " x " + i + " = " + (CINCO * i));
		}
		System.out.println("\n");

	}

}
